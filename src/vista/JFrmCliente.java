    public void listar(){
        DaoPersona dao=new DaoPersona();
        ArrayList<Cliente> lista_personas = dao.listar();
        
        DefaultTableModel modelo=new DefaultTableModel();
        tblLista.setModel(modelo);
        modelo.addColumn("RUT");
        modelo.addColumn("NOMBRE_COMPLETO");
        modelo.addColumn("DIRECCION");
        modelo.addColumn("COMUNA");
        modelo.addColumn("TELEFONO");
        modelo.addColumn("CORREO");
        
        for (Cliente item : lista_personas) {
            Object[] fila = new Object[6];
            fila[0] = item.getRut();
            fila[1] = item.getNombre_completo();
            fila[2] = item.getDireccion();
            fila[3] = item.getComuna();
            fila[4] = item.getTelefono();
            fila[5] = item.getCorreo();
            
            modelo.addRow(fila);
        }
        
    }
