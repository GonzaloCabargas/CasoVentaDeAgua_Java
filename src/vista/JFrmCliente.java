    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DaoPersona dao = new DaoPersona();
        Cliente cli = dao.buscar(txtRut.getText());
        if (cli != null) {
            txtNomCom.setText(cli.getNombre_completo());
            txtDireccion.setText(cli.getDireccion());
            txtComuna.setText(cli.getComuna());
            //txtTelefono.getText(cli.getTelefono());
            //txtTelefono.requestFocus(/*cli.getTelefono()*/);
            txtCorreoElectronico.setText(cli.getCorreo());
            
        } else {
            JOptionPane.showMessageDialog(null, "RUT no encontrado.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

