    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String rut = txtRut.getText();
        String nombre_completo = txtNomCom.getText();
        String direccion = txtDireccion.getText();
        String comuna = txtComuna.getText();
        int telefono = Integer.parseInt(txtTelefono.getText());
        String correo = txtCorreoElectronico.getText();

        Cliente cli = new Cliente(rut, nombre_completo, direccion, comuna, telefono, correo);

        DaoPersona dao = new DaoPersona();

        boolean respuesta = dao.grabar(cli);

        if (respuesta == true) {
            JOptionPane.showMessageDialog(null, "Datos almacenados.");
            listar();
            
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo almacenar los datos.");
        } 
        
        txtRut.setText("");
        txtNomCom.setText("");
        txtDireccion.setText("");
        txtComuna.setText("");
        txtTelefono.setText("");
        txtCorreoElectronico.setText("");
        
        
    }//GEN-LAST:event_btnIngresarActionPerformed

